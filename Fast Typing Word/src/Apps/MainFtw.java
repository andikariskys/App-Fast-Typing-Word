    private void kata() {
        Random rand = new Random();
        Random plhBhs = new Random();
        int rn4indo, rn5indo, rn6indo, rn7indo;
        int rn4ingg, rn5ingg, rn6ingg, rn7ingg;
        
        // membuat object class Kata
        Kata kt = new Kata();
        // memasukkan data dari class ke variabel yang telah dibuat
        indo4kata = kt.indo4kata;
        indo5kata = kt.indo5kata;
        indo6kata = kt.indo6kata;
        indo7kata = kt.indo7kata;
        eng4kata = kt.eng4kata;
        eng5kata = kt.eng5kata;
        eng6kata = kt.eng6kata;
        eng7kata = kt.eng7kata;
        
        rn4indo = rand.nextInt(indo4kata.length);
        rn5indo = rand.nextInt(indo5kata.length);
        rn6indo = rand.nextInt(indo6kata.length);
        rn7indo = rand.nextInt(indo7kata.length);
        rn4ingg = rand.nextInt(eng4kata.length);
        rn5ingg = rand.nextInt(eng5kata.length);
        rn6ingg = rand.nextInt(eng6kata.length);
        rn7ingg = rand.nextInt(eng7kata.length);
        
        pilihBahasa = plhBhs.nextInt(2);
        
        if (userBahasaJml == 1) {
            if (userJmlHuruf == 4) {
                labelKata.setText(indo4kata[rn4indo]);
            } else if (userJmlHuruf == 5) {
                labelKata.setText(indo5kata[rn5indo]);
            } if (userJmlHuruf == 6) {
                labelKata.setText(indo6kata[rn6indo]);
            } if (userJmlHuruf == 7) {
                labelKata.setText(indo7kata[rn7indo]);
            }
        } if (userBahasaJml == 2) {
            if (userJmlHuruf == 4) {
                labelKata.setText(eng4kata[rn4ingg]);
            } else if (userJmlHuruf == 5) {
                labelKata.setText(eng5kata[rn5ingg]);
            } if (userJmlHuruf == 6) {
                labelKata.setText(eng6kata[rn6ingg]);
            } if (userJmlHuruf == 7) {
                labelKata.setText(eng7kata[rn7ingg]);
            }
        } else if (userBahasaJml == 3) {
            if (userJmlHuruf == 4) {
                if (pilihBahasa == 0) {
                    labelKata.setText(indo4kata[rn4indo]);
                } else if (pilihBahasa == 1) {
                    labelKata.setText(eng4kata[rn4ingg]);
                }
            } else if (userJmlHuruf == 5) {
                if (pilihBahasa == 0) {
                    labelKata.setText(indo5kata[rn5indo]);
                } else if (pilihBahasa == 1) {
                    labelKata.setText(eng5kata[rn5ingg]);
                }
            } else if (userJmlHuruf == 6) {
                if (pilihBahasa == 0) {
                    labelKata.setText(indo6kata[rn6indo]);
                } else if (pilihBahasa == 1) {
                    labelKata.setText(eng6kata[rn6ingg]);
                }
            } else if (userJmlHuruf == 7) {
                if (pilihBahasa == 0) {
                    labelKata.setText(indo7kata[rn7indo]);
                } else if (pilihBahasa == 1) {
                    labelKata.setText(eng7kata[rn7ingg]);
                }
            }
        } 
    }
   
    private void countDown() {
        timer = new Timer(1000, (ActionEvent e) -> {
            progress--;
            Waktu--;
            
            if (Waktu == 0) {
                timer.stop();
                timerGanti.stop();
                JOptionPane.showMessageDialog(null, "Game Berakhir\n"
                        + "Jumlah kata permenit : " + jmlKata + "\n"
                                + "Akurasi mengetik : " + progress);
                HomeFtw hf = new HomeFtw();
                hf.setVisible(true);
                this.setVisible(false);
            } else {
                progresbarKata.setValue(progress);
                labelTimer.setText(String.valueOf(Waktu));
            }
            
            if (progress < 0) {
                progress = 0;
            }
        });
        timer.start();
    }

    private void mGantiKata() {
        timerGanti = new Timer(1000, (ActionEvent e) -> {
            gantiKata--;
            
            if (gantiKata == 0) {
                kata();
                tbKata.setText("");
                gantiKata = userTimer;
            }
        });
        timerGanti.start();
    }
