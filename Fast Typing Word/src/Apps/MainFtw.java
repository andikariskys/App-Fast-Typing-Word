    private void tbKataKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tbKataKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (tbKata.getText().trim().equals(labelKata.getText().trim())) {
                tbKata.setText("");
                kata();
                if (progress + 2 < maxProg) {
                    progress = progress + 2;
                    gantiKata = userTimer;
                }
                jmlKata = jmlKata + 1;
                labelJmlkata.setText(String.valueOf(jmlKata));
            } else {
                tbKata.setText("");
            }
        }
    }//GEN-LAST:event_tbKataKeyPressed

